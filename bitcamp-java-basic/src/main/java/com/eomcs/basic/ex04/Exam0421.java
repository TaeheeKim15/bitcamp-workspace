package com.eomcs.basic.ex04;

public class Exam0421 {
  public static void main(String[] args) {
    
    float f;
    
    f = 9.876545f; // 소수점을 떼면 숫자의 개수가 7개
    System.out.println(f);
    
    f = 987654.5f;
    System.out.println(f);
    
    f = 0.000009876545f;
    System.out.println(f);
    
    f = 9.8765456f;
    System.out.println(f);
    
    f = 9876545.6f;
    System.out.println(f);
  }
}
