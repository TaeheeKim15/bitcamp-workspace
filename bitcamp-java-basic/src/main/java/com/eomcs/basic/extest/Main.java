import java.util.Scanner;

class Main {
  public static void main(String[] args) {

    Scanner keyScan = new Scanner(System.in);

    int A = keyScan.nextInt();

    int B = keyScan.nextInt();


    if (A > 0 && 10 > B)
      for (;;) {
          System.out.println(A + B);

          break;
        }
        System.out.println();

        keyScan.close();
      }
  }

